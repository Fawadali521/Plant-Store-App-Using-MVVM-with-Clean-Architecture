class CartDetails {
  final String imageUrl;
  final String typeName;
  final String name;
  final String price;
  final String description;
  final String id;
  final String about;
  final String height;
  final String humidity;
  final String width;
  CartDetails({
    required this.id,
    required this.imageUrl,
    required this.typeName,
    required this.name,
    required this.price,
    required this.description,
    required this.about,
    required this.height,
    required this.humidity,
    required this.width,
  });
}
